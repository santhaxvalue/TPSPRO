package com.panelManagement.model;

import java.io.Serializable;

public class CityListModel implements Serializable {
    private static final long serialVersionUID = 1L;
    int CityAnsID;
    String CityName = "";

    public CityListModel(int cityAnsID, String cityName) {
        super();
        CityAnsID = cityAnsID;
        CityName = cityName;
    }

    public int getCityAnsID() {
        return CityAnsID;
    }

    public void setCityAnsID(int cityAnsID) {
        CityAnsID = cityAnsID;
    }

    public String getCityName() {
        return CityName;
    }

    public void setCityName(String cityName) {
        CityName = cityName;
    }

}
